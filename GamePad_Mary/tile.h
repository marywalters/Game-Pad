void drawLevel(int thisLevel) {
  for (int y = 0; y < tileH; y++) {
    for (int x = 0; x < tileW; x++) {
      int index = x + (y * tileW);
      int whichTile = levels[thisLevel][index];

      int finalX = x * tileSize;
      int finalY = y * tileSize;
      tft.drawRGBBitmap(finalX, finalY, sprites_PIX[whichTile], tileSize, tileSize);
    }
  }
  //tft.fillRect(0, screenH - UIHeight, screenW, screenH, ILI9341_BLACK);
}
